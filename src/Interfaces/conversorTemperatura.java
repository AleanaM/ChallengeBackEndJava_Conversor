public void calculos(double gradosIniciales, int cInicial, int cFinal){


    if(cInicial==0){
        
        if(cFinal==1){
            gradosFinales = (gradosIniciales * 9/5) + 32;
        }
        
        else{
            if(cFinal==2){
                gradosFinales = (gradosIniciales + 273.15);
            }
            
            else{gradosFinales=gradosIniciales;}            
        }
    }
    
    else{

        if(cInicial==1){

            if(cFinal==0){
                gradosFinales = (gradosIniciales - 32) * 5/9;
            }

            else{
                if(cFinal==2){
                    gradosFinales = (gradosIniciales -32) * 5/9 + 273.15;
                }
                
                else{gradosFinales=gradosIniciales;}
            }
        }
        
        else{

            if(cInicial==2){

                if(cFinal==0){
                    gradosFinales = (gradosIniciales - 273.15);
                }

                else{
                    if(cFinal==1){
                        gradosFinales = (gradosIniciales - 273.15) * 9/5 + 32;
                    }
                    
                    else{gradosFinales=gradosIniciales;}                    
                }
            }                    
        }        
    }
    
    DecimalFormat formato = new DecimalFormat("#.####");
    temperaturaResultado.setText(String.valueOf(formato.format(gradosFinales)));
}



