    private void temperaturaInicialKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_temperaturaInicialKeyTyped
     
        char []soloNumeros = {'1','2','3','4','5','6','7','8','9','0','.'};

        int comparador=0;

        for(int i=0;i<=10;i++){
            if (soloNumeros[i]==evt.getKeyChar()){
                comparador=1;
            }
        }

        if(comparador==0) {
            evt.consume();  
            getToolkit().beep(); 
         }    
        
    }//GEN-LAST:event_temperaturaInicialKeyTyped

